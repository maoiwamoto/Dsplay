 version :thumb do
  process resize_to_limit: [450, 900]
 end
 
  version :tiny do
    process :crop
    process resize_to_fill: [120, 120]
  end
  
  def crop
    if model.crop_x.present?
      resize_to_limit(450, 900)
      manipulate! do |img|
        x = model.crop_x.to_i
        y = model.crop_y.to_i
        w = model.crop_w.to_i
        h = model.crop_h.to_i
        # [[w, h].join('x'),[x, y].join('+')].join('+') => "wxh+x+y"
        img.crop([[w, h].join('x'),[x, y].join('+')].join('+'))
      end
    end
  end

